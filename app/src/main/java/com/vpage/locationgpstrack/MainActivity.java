    @Override
    public void onChange(String status) {
        Log.d(TAG, "Network Availability: "+status);
        boolean netStatus=false;
        switch (status) {
            case "Connected to Internet with Mobile Data":
                netStatus = true;
                break;
            case "Connected to Internet with WIFI":
                netStatus = true;
                break;
            default:
                netStatus = false;
                break;
        }
        Log.d(TAG, "isNetworkAvailable: "+netStatus);
        getTrackedDataFromDB(netStatus);
    }


    public  void checkInternetStatus(){
        String status = NetworkUtil.getConnectivityStatusString(getApplicationContext());
        boolean isNetworkAvailable;
        switch (status) {
            case "Connected to Internet with Mobile Data":
                isNetworkAvailable = true;
                break;
            case "Connected to Internet with WIFI":
                isNetworkAvailable = true;
                break;
            default:
                isNetworkAvailable = false;
                break;
        }
        Log.d(TAG, "isNetworkAvailable: "+isNetworkAvailable);
        getTrackedDataFromDB(isNetworkAvailable);
    }


    void getTrackedDataFromDB(boolean isNetworkAvailable){
        if(isNetworkAvailable && locationTrackList != null){

            Log.d(TAG, "LocationTrackList: "+locationTrackList.toString());
            // Creating an instance of LocationDeleteTask
            LocationDeleteTask deleteTask = new LocationDeleteTask();

            // Deleting all the rows from SQLite database table
            deleteTask.execute();
        }
    }

